// Подключаем библиотеку Mongoose. 
// Она как "переводчик": позволяет нам писать на обычном JavaScript, 
// а сама переводит это в сложные команды для базы данных MongoDB.
const mongoose = require('mongoose');

// Создаем "Схему" (Schema). 
// Представь, что это пустой бланк анкеты. Пока юзер его не заполнит правильно, 
// база данных не создаст для него "личное дело" (документ).
const userSchema = new mongoose.Schema({
  
  // ПОЛЕ: Имя пользователя (Никнейм)
  username: { 
    type: String,      // Тип данных — строка (текст)
    required: true,    // Обязательное поле. Если при регистрации не прислать имя — будет ошибка.
    
    // ПРО ПОВТОРЫ: 
    // Если тут написать 'unique: true', то база не даст создать двух "TechnoShaman".
    // Если ты хочешь, чтобы ники ДУБЛИРОВАЛИСЬ (как в Discord), 
    // просто удали строчку 'unique: true' или поставь 'false'.
    unique: true,      
    
    trim: true         // Полезная штука: удаляет пробелы по бокам. 
                       // Если юзер случайно ввел " Shaman ", в базу запишется "Shaman".
  },

  // ПОЛЕ: Пароль
  // ВНИМАНИЕ: Мы никогда не храним пароли типа "qwerty123". 
  // В это поле контроллер пришлет "хэш" — длинную абракадабру (шифр).
  password: { 
    type: String, 
    required: true    // Без пароля аккаунт не создать — это логично.
  },

  // ПОЛЕ: Аватарка
  // Сюда мы будем записывать ссылку на картинку из Cloudinary.
  avatar: {
    type: String,
    // Если юзер не загрузил фото, ставим картинку по умолчанию.
    default: 'https://cdn-icons-png.flaticon.com/512/149/149071.png' 
  },

  // ПОЛЕ: Дата регистрации
  createdAt: {
    type: Date,
    default: Date.now // JavaScript сам подставит текущее время в момент создания.
  },

  // ПОЛЕ: Статус (В сети / Не в сети)
  status: {
    type: String,
    enum: ['online', 'offline'],
    default: 'offline'
  }

  // СОВЕТ ПО УЛУЧШЕНИЮ (ПРО ID):
  // Тебе НЕ НУЖНО писать тут поле "id". 
  // MongoDB автоматически создаст для каждого юзера поле "_id".
  // Это уникальный набор цифр и букв, который НИКОГДА не повторяется.
  // Даже если у двух людей будут одинаковые ники "Ivan", их "_id" будут разными.
  // По этому "_id" мы и будем делать ПЕРСОНАЛЬНЫЕ чаты.

}, { 
  // Эта настройка убирает лишнее поле "__v" (версия документа), которое MongoDB лепит по умолчанию.
  versionKey: false 
});

// Экспортируем (выносим наружу) эту модель под именем 'User'.
// Теперь в любом другом файле мы сможем написать "require('./models/User')" 
// и работать с пользователями в базе.
module.exports = mongoose.model('User', userSchema);