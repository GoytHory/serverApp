// ЗАВИСИМОСТИ:
// 1. Подключаем модель пользователя. 
// Это наш "чертеж" из папки models. Через него мы будем искать и сохранять людей в базе.
const User = require('../models/User');

// 2. Подключаем библиотеку для шифрования.
// Мы НЕ храним пароли в чистом виде. bcrypt превратит "12345" в что-то типа "$2a$10$asdf...".
const bcrypt = require('bcryptjs');

/**
 * ФУНКЦИЯ РЕГИСТРАЦИИ (register)
 * Эта функция сработает, когда кто-то нажмет кнопку "Зарегистрироваться" в приложении.
 */
exports.register = async (req, res) => {
    try {
        // req.body — это данные, которые прислал нам телефон (фронтенд).
        // Мы "вытаскиваем" из них присланные ник и пароль.
        const { username, password } = req.body;

        // ШАГ 1: Проверка на вшивость.
        // Идем в базу данных и ищем: а нет ли там уже юзера с таким ником?
        // Мы обращаемся к модели User и методу findOne (найти одного).
        const candidate = await User.findOne({ username });

        if (candidate) {
            // Если нашли — тормозим процесс. 400 — это ошибка "Плохой запрос".
            return res.status(400).json({ 
                message: "Дружище, этот ник уже занят. Придумай что-то поинтереснее!" 
            });
        }

        // ШАГ 2: Шифрование пароля.
        // Мы берем обычный пароль и "солим" его. 7 — это сложность шифрования.
        // hash — это асинхронная операция (занимает время), поэтому пишем await.
        const hashedPassword = await bcrypt.hash(password, 7);

        // ШАГ 3: Создание записи в базе.
        // Создаем новый объект на основе нашей "анкеты" (схемы User).
        // Вместо обычного пароля подставляем наш шифр (hashedPassword).
        const user = new User({
            username,
            password: hashedPassword,
            status: 'online' // Сразу ставим статус 'в сети' при регистрации
        });

        // Сохраняем это дело в MongoDB Atlas.
        await user.save();

        // Отправляем радостный ответ фронтенду. 210 или 201 — это код "Создано".
        return res.status(201).json({ 
            message: "Поздравляю! Ты в системе. Теперь можешь войти.",
            userId: user._id // Отдаем уникальный ID, который база создала САМА
        });

    } catch (error) {
        // Если что-то пошло не так (например, отвалился интернет у сервера)
        console.error("Ошибка в контроллере регистрации:", error);
        res.status(500).json({ message: "На сервере что-то сломалось. Мы уже чиним!" });
    }
};

/**
 * УЛУЧШЕНИЯ НА БУДУЩЕЕ:
 * 1. Валидация: Можно проверять, чтобы пароль был не короче 6 символов.
 * 2. Логин: Сюда же в этот файл мы добавим функцию exports.login (вход по паролю).
 * 3. Токены: После регистрации можно сразу выдавать JWT-токен, чтобы юзеру 
 * не нужно было вводить пароль второй раз для входа.
 * 4. Обработка ошибок: Можно сделать более детальные сообщения, 
 * если, например, база данных MongoDB Atlas перегружена.
 */